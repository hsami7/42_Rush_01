#include <unistd.h>

void	print_arr(int *arr)
{
	int		i;
	char	c;

	i = 0;
	while (i < 16)
	{
		c = arr[i] + '0';
		write(1, &c, 1);
		i++;
		if (i % 4 == 0)
			write(1, "\n", 1);
		else
			write(1, " ", 1);
	}
}
